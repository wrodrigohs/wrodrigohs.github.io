                        <h2 class="wp-block-heading">Política de privacidade</h2>
                        <hr class="wp-block-separator"/>
                        <br/>
                        <p>
                            <strong>1 – Objetivo</strong>
                            <br/>
                            Para a BELLOCARD – Administradora de Cartões LTDA, a privacidade e segurança são
                            prioridades e nos comprometemos com a transparência do tratamento de dados pessoais dos
                            nossos usuários/clientes. Por isso, está presente Política de Privacidade estabelece como regras e
                            padrões de como é feita a coleta, uso e transferência de informações de clientes ou outras pessoas
                            que acessam ou usam nossas aplicações.
                            Ao utilizar nossos serviços, você entende que coletaremos e usaremos suas informações
                            pessoais nas formas descritas nesta Política, sob as normas da Constituição Federal de 1988 (art. 5º,
                            LXXIX; e o art. 22º, XXX – incluídos pela EC 115/2022), das normas de Proteção de Dados (LGPD, Lei
                            Federal 13.709/2018), das disposições consumeristas da Lei Federal 8078/1990 e as demais normas
                            do ordenamento jurídico brasileiro aplicáveis.
                            Dessa forma, a BELLOCARD – Administradora de Cartões LTDA, doravante denominada
                            BELLOCARD, inscrita no CNPJ/MF sob o nº 33.735.789/0001-88, no papel de Controladora de Dados,
                            obriga-se ao disposto na presente Política de Privacidade.
                        </p>
                        <br/>
                        <p>
                            <strong>2. Dados Coletados</strong><br/>
                            Nossas aplicações coletam e utilizam dados pessoais e/ou empresariais de seus
                            clientes/associados, lojistas, sindicatos e empresas, de forma a viabilizar a prestação de serviços.
                            <strong><br/>2.1. Dados fornecidos pelos Clientes/Associados</strong><br/>
                            Endereço completo, nome completo, nome da mãe, nome do pai, salário, dados bancários
                            (banco, agência e conta), telefone fixo e celular, título de eleitor, zona e seção, empresa que
                            trabalha, data de admissão e data de demissão.
                            <strong><br/>2.2. Dados fornecidos pelos Lojistas</strong><br/>
                            Endereço Completo, Razão Social, Nome Fantasia, Telefone fixo, Celular e Fax, e-mail,
                            atividade do Lojista, Informações Bancárias (banco, agência, conta e Chave PIX).
                            <strong><br/>2.3. Dados fornecidos pelos Sindicatos</strong><br/>
                            Endereço Completo, Razão Social, Nome Fantasia, Telefone fixo, Celular e Fax, e-mail.
                            <strong><br/>2.4. Dados fornecidos pelos Empresas</strong><br/>
                            Endereço Completo, Razão Social, Nome Fantasia, CNPJ, Telefone fixo, Celular e Fax, e-mail.
                            <strong><br/>2.5. Informação coletadas pelas nossas aplicações WEB e Aplicativos</strong><br/>
                            As informações coletadas pelos nossos aplicativos e aplicações incluirão todas ou apenas
                            algumas das abaixo descritas, dependendo de quais e como você optar por usar.
                            <br/><br/>
                            <strong>a)</strong> Geolocalização e Endereço IP – Informações detalhadas sobre a geolocalização, sob a
                            forma de sinais GPS, endereço IP e outras informações relacionadas enviadas pelo dispositivo móvel
                            no qual o aplicativo está instalado e ativado;
                            <strong><br/>b)</strong> Detalhes do Aparelho – Informações de status de bateria, modelo do aparelho, sistema
                            operacional e versão utilizados, apps instalados e tipo de conexão utilizada (rede móvel de dados
                            ou wi-fi);
                            <strong><br/>c)</strong> Android Device ID – Identificador único que cada aparelho com sistema operacional
                            Android possui;
                            <strong><br/>d)</strong> Dados de acesso – Para fins exclusivos de permitir o acesso aos serviços relacionados
                            do Google, os apps poderão solicitar dados de acesso à conta Google vinculada ao app.
                        </p>
                        <br/>
                        <p>
                            <strong>3. Uso dos Dados e Informações Coletadas</strong><br/>
                            <strong>3.1 – Uso nos Aplicativos</strong><br/>
                            Alguns dados coletados são exclusivamente utilizados para manter o correto
                            funcionamento dos aplicativos, como por exemplo o Android Device ID, item fundamental para a
                            comunicação do seu dispositivo com nossos servidores, onde através do mesmo seu dispositivo
                            poderá ser identificado unicamente para que as informações possam ser enviadas aos nossos
                            servidores e retornadas corretamente para o seu aparelho e aplicativo.
                            <br/><strong>3.2 – Uso na Aplicação WEB</strong><br/>
                            Os dados são coletados exclusivamente utilizados para manter o correto funcionamento
                            da aplicação.
                            <br/><strong>3.3 – Outros</strong><br/>
                            Alguns dos dados fornecidos já citados nessa política poderão também vir a ser utilizados
                            exclusivamente para auditoria e análise interna, para nos ajudar a desenvolver, melhorar nossos
                            serviços e conteúdos disponibilizados.
                        </p>
                        <br/>
                        <p>
                            <strong>4. Base de Dados</strong><br/>
                            <strong>4.1 – Banco de Dados do Aplicativo</strong><br/>
                            As informações inseridas pelos usuários são para uso exclusivo em nossos aplicativos e
                            são armazenadas em banco de dados local no seu aparelho e após a desinstalação do aplicativo as
                            informações são apagadas. Contudo, ressaltamos que sua conta do Google pode possuir
                            Configurações de backup automático de alguns desses dados por você informados. Se por ventura
                            você não deseje realizar o backup automático desses dados, você poderá desativar essa
                            funcionalidade manualmente nas configurações do seu sistema Android ou acessando suas
                            configurações de conta.
                            <br/><strong>4.2 – Banco de Dados da Aplicação WEB</strong><br/>
                            As informações inseridas pelos colaboradores são para uso exclusivo em nossa aplicação
                            e são armazenadas em bancos de dados em nosso parque tecnológico com acesso exclusivo apenas
                            por nossos técnicos.
                        </p>
                        <br/>
                        <p>
                            <strong>5. Consentimento</strong><br/>
                            É a partir do consentimento do usuário do aplicativo e/ou aplicação BelloCard que tratamos
                            os seus dados pessoais. O consentimento é a manifestação livre, informada e inequívoca pela qual
                            você autoriza a BELLOCARD a tratar seus dados.
                            Assim, em consonância com a Lei Geral de Proteção de Dados, seus dados só serão
                            coletados, tratados e armazenados mediante prévio e expresso consentimento.
                            O seu consentimento será obtido de forma específica para cada finalidade acima descrita,
                            evidenciando o compromisso de transparência e boa-fé da BelloCard para com seus
                            usuários/clientes, seguindo as regulações legislativas pertinentes.
                            Ao utilizar os serviços da BelloCard e fornecer seus dados pessoais, você está ciente e
                            consentindo com as disposições desta Política de Privacidade, além de conhecer seus direitos e
                            como exercê-los.
                            A qualquer tempo e sem nenhum custo, você poderá revogar seu consentimento.
                            É importante destacar que a revogação do consentimento para o tratamento dos dados
                            pode implicar a impossibilidade da performance adequada de alguma funcionalidade do site que
                            dependa da operação. Tais consequências serão informadas previamente.
                        </p>
                        <br/>
                        <p>
                            <strong>6. Quais são os seus direitos?</strong><br/>
                            A BELLOCARD assegura a seus usuários/clientes seus direitos de titular previstos no artigo
                            18 da Lei Geral de Proteção de Dados. Dessa forma, você pode, de maneira gratuita e a qualquer
                            tempo:
                            <br/><br/><strong>a.</strong> Confirmar a existência de tratamento de dados, de maneira simplificada ou em formato
                            claro e completo, mediante solicitação ao nosso suporte.
                            <br/><strong>b.</strong> Acessar seus dados, podendo solicitá-los em uma cópia legível sob forma impressa ou
                            por meio eletrônico, seguro e idôneo.
                            <br/><strong>c.</strong> Corrigir seus dados, ao solicitar a edição, correção ou atualização destes.
                            <br/><strong>d.</strong> Limitar seus dados quando desnecessários, excessivos ou tratados em
                            desconformidade com a legislação através da anonimização, bloqueio ou eliminação.
                            <br/><strong>e.</strong> Solicitar a portabilidade de seus dados, através de um relatório de dados cadastrais que
                            a BELLOCARD trata a seu respeito.
                            <br/><strong>f.</strong> Eliminar seus dados tratados a partir de seu consentimento, exceto nos casos previstos
                            em lei.
                            <br/><strong>g.</strong> Revogar seu consentimento, desautorizando o tratamento de seus dados.
                            <br/><strong>h.</strong> Informar-se sobre a possibilidade de não fornecer seu consentimento e sobre as
                            consequências da negativa.
                        </p>
                        <br/>
                        <p>
                            <strong>7. Como você pode exercer seus direitos?</strong><br/>
                            Para exercer seus direitos de titular, você deve entrar em contato com a (nome empresarial
                            simplificado) através dos seguintes meios disponíveis:
                            <br/><br/>• E-mail: <a href="mailto:adm@bellocard.com.br">adm@bellocard.com.br</a>
                            <br/>• Telefones: (67) 3326-6510 | (67) 9 9892-9178
                            <br/><br/>De forma a garantir a sua correta identificação como titular dos dados pessoais objeto da
                            solicitação, é possível que solicitemos documentos ou demais comprovações que possam
                            comprovar sua identidade. Nessa hipótese, você será informado previamente.
                        </p>
                        <br/>
                        <p>
                            <strong>8. Como e por quanto tempo seus dados serão
                            armazenados?</strong><br/>
                            Seus dados pessoais coletados pela BELLOCARD serão utilizados e armazenados durante o
                            tempo necessário para a prestação do serviço ou para que as finalidades elencadas na presente
                            Política de Privacidade sejam atingidas, considerando os direitos dos titulares dos dados e dos
                            controladores.
                            De modo geral, seus dados serão mantidos enquanto a relação contratual entre você e
                            a BELLOCARD perdurar. Findado o período de armazenamento dos dados pessoais, estes serão
                            excluídos de nossas bases de dados ou anonimizados, ressalvadas as hipóteses legalmente previstas
                            no artigo 16 lei geral de proteção de dados, a saber:
                            <br/><br/><strong>I</strong> – Cumprimento de obrigação legal ou regulatória pelo controlador;
                            <br/><strong>II</strong> – Estudo por órgão de pesquisa, garantida, sempre que possível, a anonimização dos dados
                            pessoais;
                            <br/><strong>III</strong> – Transferência a terceiro, desde que respeitados os requisitos de tratamento de dados
                            dispostos nesta Lei; ou
                            <br/><strong>IV</strong> – Uso exclusivo do controlador, vedado seu acesso por terceiro, e desde que anonimizados
                            os dados.
                            <br/><br/>Isto é, informações pessoais sobre você que sejam imprescindíveis para o cumprimento de
                            determinações legais, judiciais e administrativas e/ou para o exercício do direito de defesa em
                            processos judiciais e administrativos serão mantidas, a despeito da exclusão dos demais dados.
                            O armazenamento de dados coletados pela BELLOCARD reflete o nosso compromisso com a
                            segurança e privacidade dos seus dados. Empregamos medidas e soluções técnicas de proteção
                            aptas a garantir a confidencialidade, integridade e inviolabilidade dos seus dados. Além disso,
                            também contamos com medidas de segurança apropriadas aos riscos e com controle de acesso às
                            informações armazenadas.
                        </p>
                        <br/>
                        <p>
                            <strong>9. O que fazemos para manter seus dados seguros?</strong><br/>
                            Para mantermos suas informações pessoais seguras, usamos ferramentas físicas, eletrônicas
                            e gerenciais orientadas para a proteção da sua privacidade.
                            Aplicamos essas ferramentas levando em consideração a natureza dos dados pessoais
                            coletados, o contexto e a finalidade do tratamento e os riscos que eventuais violações gerariam para
                            os direitos e liberdades do titular dos dados coletados e tratados.
                            Entre as medidas que adotamos, destacamos as seguintes:
                            <br/><br/><strong>a.</strong> Apenas pessoas autorizadas têm acesso a seus dados pessoais
                            <br/><strong>b.</strong> O acesso a seus dados pessoais é feito somente após o compromisso de
                            confidencialidade
                            <br/><strong>c.</strong> Seus dados pessoais são armazenados em ambiente seguro e idôneo.
                            <br/><br/>A BELLOCARD se compromete a adotar as melhores posturas para evitar incidentes de
                            segurança. Contudo, é necessário destacar que nenhuma página virtual é inteiramente segura e
                            livre de riscos. É possível que, apesar de todos os nossos protocolos de segurança, problemas de
                            culpa exclusivamente de terceiros ocorram, como ataques cibernéticos de hackers, ou também em
                            decorrência da negligência ou imprudência do próprio usuário/cliente.
                            Em caso de incidentes de segurança que possa gerar risco ou dano relevante para você ou
                            qualquer um de nossos usuários/clientes, comunicaremos aos afetados e a Autoridade Nacional de
                            Proteção de Dados sobre o ocorrido, em consonância com as disposições da Lei Geral de Proteção
                            de Dados.
                        </p>
                        <br/>
                        <p>
                            <strong>10. Cookies ou dados de navegação</strong><br/>
                            A BELLOCARD faz uso de Cookies, que são arquivos de texto enviados pela plataforma ao seu
                            computador e que nele se armazenam, que contém informações relacionadas à navegação do site.
                            Em suma, os Cookies são utilizados para aprimorar a experiência de uso.
                            Ao acessar nosso site e consentir com o uso de Cookies, você manifesta conhecer e aceitar
                            a utilização de um sistema de coleta de dados de navegação com o uso de Cookies em seu
                            dispositivo.
                            Você pode, a qualquer tempo e sem nenhum custo, alterar as permissões, bloquear ou
                            recusar os Cookies. Todavia, a revogação do consentimento de determinados Cookies pode
                            inviabilizar o funcionamento correto de alguns recursos da plataforma.
                            Para gerenciar os cookies do seu navegador, basta fazê-lo diretamente nas configurações do
                            navegador, na área de gestão de Cookies. Você pode acessar tutoriais sobre o tema diretamente
                            nos links abaixo:
                            <br/><br/><a href="https://support.microsoft.com/pt-br/windows/excluir-e-gerenciar-cookies-168dab11-0753-043d-7c16-ede5947fc64d">Se você usa o Internet Explorer.</a>
                            <br/><a href="https://support.mozilla.org/pt-BR/kb/gerencie-configuracoes-de-armazenamento-local-de-s">Se você usa o Firefox.</a>
                            <br/><a href="https://support.apple.com/pt-br/guide/safari/sfri11471/mac">Se você usa o Safari.</a>
                            <br/><a href="https://support.google.com/chrome/answer/95647?co=GENIE.Platform%3DDesktop&oco=1&hl=pt-BR">Se você usa o Google Chrome.</a>
                            <br/><a href="https://support.microsoft.com/pt-br/microsoft-edge/excluir-cookies-no-microsoft-edge-63947406-40ac-c3b8-57b9-2a946a29ae09">Se você usa o Microsoft Edge.</a>
                            <br/><a href="https://help.opera.com/en/latest/web-preferences/#cookies">Se você usa o Opera.</a>
                        </p>
                        <br/>
                        <p>
                            <strong>11. Alteração desta Política de Privacidade</strong><br/>
                            Reservamos o direito de modificar essa Política de Privacidade a qualquer tempo,
                            principalmente em função da adequação a eventuais alterações feitas em nosso aplicativo e/ou
                            aplicação ou em âmbito legislativo. Recomendamos que você a revise com frequência.
                            Eventuais alterações entrarão em vigor a partir de sua publicação em nosso site e sempre
                            lhe notificaremos acerca das mudanças ocorridas.
                            Ao utilizar nossos serviços e fornecer seus dados pessoais após tais modificações, você as
                            consente.
                        </p>
                        <br/>
                        <p>
                            <strong>12. Responsabilidade</strong><br/>
                            A BELLOCARD prevê a responsabilidade dos agentes que atuam nos processos de
                            tratamento de dados, em conformidade com os artigos 42 ao 45 da Lei Geral de Proteção de Dados.
                            Nos comprometemos em manter esta Política de Privacidade atualizada, observando suas
                            disposições e zelando por seu cumprimento.
                            Além disso, também assumimos o compromisso de buscar condições técnicas e organizativas
                            seguramente aptas a proteger todo o processo de tratamento de dados.
                            Caso a Autoridade Nacional de Proteção de Dados exija a adoção de providências em relação
                            ao tratamento de dados realizado pela BELLOCARD, comprometemo-nos a segui-las.
                            <strong><br/>12.1 Isenção de responsabilidade</strong><br/>
                            Conforme mencionado no Tópico 9, embora adotemos elevados padrões de segurança a fim
                            de evitar incidentes, não há nenhuma página virtual inteiramente livre de riscos. Nesse sentido,
                            a BELLOCARD não se responsabiliza por:
                            <br/><br/><strong>I</strong> – Quaisquer consequências decorrentes da negligência, imprudência ou imperícia dos
                            usuários em relação a seus dados individuais. Garantimos e nos responsabilizamos apenas pela
                            segurança dos processos de tratamento de dados e do cumprimento das finalidades descritas no
                            presente instrumento.
                            Destacamos que a responsabilidade em relação à confidencialidade dos dados de acesso é
                            do usuário.
                            <br/><strong>II</strong> – Ações maliciosas de terceiros, como ataques de hackers, exceto se comprovada conduta
                            culposa ou deliberada da BELLOCARD.
                            Destacamos que em caso de incidentes de segurança que possam gerar risco ou dano
                            relevante para você ou qualquer um de nossos usuários/clientes, comunicaremos aos afetados e a
                            Autoridade Nacional de Proteção de Dados sobre o ocorrido e cumpriremos as providências
                            necessárias.
                            <br/><strong>III</strong> – Inveracidade das informações inseridas pelo usuário/cliente nos registros necessários
                            para a utilização dos serviços da BELLOCARD; quaisquer consequências decorrentes de informações
                            falsas ou inseridas de má-fé são de inteiramente responsabilidade do usuário/cliente.
                        </p>
